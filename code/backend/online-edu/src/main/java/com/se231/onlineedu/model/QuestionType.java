package com.se231.onlineedu.model;

/**
 * Question Type Enum
 *
 * enum of questions' type
 *
 * @author Yuxuan Liu
 *
 * @date 2019/7/4
 */
public enum QuestionType {
    /**
     * true of false questions
     */
    T_OR_F,

    /**
     * Single choice question
     */
    SINGLE_ANSWER,

    /**
     * multiple choices question
     */
    MULTIPLE_ANSWER,

    /**
     * subjective question
     */
    SUBJECTIVE
}
